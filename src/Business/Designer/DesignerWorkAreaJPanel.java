    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) ProductTable1.getModel();
        dtm.setRowCount(0);

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            
            Object[] row = new Object[8];
            row[0] = wr;
            row[1] = wr.getProduct().getProdType();
            row[2] = wr.getSender().getEmployee();
            row[3] = wr.getReceiver() == null ? null : wr.getReceiver().getEmployee().getName();
            row[4] = wr.getProduct().getQuantity();
            row[5] = wr.getStatus();
            dtm.addRow(row);

        }
    }

