    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       int selectedRow = ProductTable1.getSelectedRow();

       if (selectedRow < 0) {
           JOptionPane.showMessageDialog(null, "Please select a row to continue");
           return;
       }

       ProvoiderRequest wr = (ProvoiderRequest) ProductTable1.getValueAt(selectedRow, 0);
       if(wr.getStatus().equalsIgnoreCase("Pending"))
       {
          JOptionPane.showMessageDialog(null,"You need to ASSIGN First!!");
       }
       else  
       {
       String prodName = String.valueOf(ProductTable1.getValueAt(selectedRow, 0));
       String prodType = (String) ProductTable1.getValueAt(selectedRow, 1);
       int quantity = Integer.parseInt(ProductTable1.getValueAt(selectedRow, 4).toString());
       
       SearchJPanel1 panel = new SearchJPanel1(userProcessContainer,system,prodName,prodType,quantity,dB4OUtil,wr,enterprise);
       userProcessContainer.add("SearchJPanel", panel);
       CardLayout layout = (CardLayout) userProcessContainer.getLayout();
       layout.next(userProcessContainer);
       }
    }//GEN-LAST:event_jButton4ActionPerformed


